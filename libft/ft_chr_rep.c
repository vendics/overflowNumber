#include "libft.h"

int		ft_chr_rep(char *str, char c)
{
	int i;
	int r;

	i = -1;
	r = 0;
	if (str == NULL)
		return (0);
	while (str[++i])
	{
		if (str[i] == c)
			r++;
	}
	return (r);
}
